		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<JiaJiao.Model.RegInfo> DataTableToList(DataTable dt)
		{
			List<JiaJiao.Model.RegInfo> modelList = new List<JiaJiao.Model.RegInfo>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				JiaJiao.Model.RegInfo model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

